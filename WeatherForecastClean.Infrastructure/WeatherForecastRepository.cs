using System;
using System.Linq;
using WeatherForecastClean.Application;
using WeatherForecastClean.Core;

namespace WeatherForecastClean.Infrastructure
{
    //THIS IS THE INFRASTRUCTURE LAYER, REPOSITORY INTERFACES WILL BE IMPLEMENTED HERE
    //DB CONTEXT FOR DATABASE CONTACT WILL BE USED HERE
    public class WeatherForecastRepository : IWeatherForecastRepository
    {
        private static readonly string[] Summaries = new[]
        {
            "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
        };

        public WeatherForecast[] GetForecasts()
        {
            var rng = new Random();
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                Date = DateTime.Now.AddDays(index),
                TemperatureC = rng.Next(-20, 55),
                Summary = Summaries[rng.Next(Summaries.Length)]
            })
            .ToArray();
        }
    }
}
